/* ヘッダファイル読み込み */
#include	<stdio.h>

/*======================================================================
	関数名			：main
	機能			：メイン処理
	入力引数説明	：なし
	戻り値			：終了情報（常に0）
	特記事項		：なし
======================================================================*/
int main(void)
{
	/* 変数定義 */
	int		in;			/* ユーザ入力データ */
	int		loop;		/* ループ可否 */
	
	
	/* 1階：化粧品売り場
	   2階：紳士服売り場
	   3階：婦人服売り場 */
	
	/* 変数初期化 */
	loop = 1;
	
	/* ループ */
	while (loop) {
		/* ユーザ入力 */
		printf("何階に行きますか？（１〜３）>");
		scanf("%d", &in);
		
		/* 入力データ判定と結果表示 */
		switch (in) {
		case 1:
			printf("化粧品売り場です\n");
			break;
		case 2:
			printf("紳士服売り場です\n");
			break;
		case 3:
			printf("婦人服売り場です\n");
			break;
		default:
			printf("その階には行けません\n");
			loop = 0;
			break;
		}
	}
	
	return 0;
}
