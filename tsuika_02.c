/* ヘッダファイル読み込み */
#include	<stdio.h>

/*======================================================================
	関数名			：main
	機能			：メイン処理
	入力引数説明	：なし
	戻り値			：終了情報（常に0）
	特記事項		：なし
======================================================================*/
int main(void)
{
	/* 変数定義 */
	int		in;		/* ユーザ入力データ */
	
	/* ユーザ入力 */
	printf("16進数で入力してください >");
	scanf("%x", &in);
	
	/* 表示 */
	printf("入力された数値は16進数で%x、10進数で%dです。\n", in, in);
	
	/* 結果return */
	return 0;
}
